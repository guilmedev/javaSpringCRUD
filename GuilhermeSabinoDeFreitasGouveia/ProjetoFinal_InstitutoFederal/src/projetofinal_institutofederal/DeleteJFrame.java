    private void jButtonExcluir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluir1ActionPerformed
        try {                                   
         //Solicita confirmarção
         int input = JOptionPane.showConfirmDialog(null, 
                "Confirmar a exclusão?", "Escolha uma opção",JOptionPane.YES_NO_OPTION);
                  
        //Se responder NÃO, não exclui
        // 0 = sim , 1= não
       if(input == 1){           
            return;            
        }else if(input == 0)
            {            
            //Instancia da conexão
            Connection connection = ConexaoDB.getInstance().getConnection();
            //Pega o nome que está no combobox
            String nome = jComboBox1.getSelectedItem().toString();
            //Passa o nome para a query
            String query = "DELETE FROM disciplinas WHERE nome = '"+nome+"'";
            //Executa a query
            connection.createStatement().executeUpdate(query);           
            //Mostra a msg de confirmação
            JOptionPane.showMessageDialog(this, "Disciplina: "+nome+" \n excluida do banco " ,"Sucesso" ,JOptionPane.INFORMATION_MESSAGE);
            //Atualiza a lista
            ListaDisciplinas();
            }                        
                              
        } catch (SQLException ex) {
            Logger.getLogger(DeleteJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButtonExcluir1ActionPerformed

