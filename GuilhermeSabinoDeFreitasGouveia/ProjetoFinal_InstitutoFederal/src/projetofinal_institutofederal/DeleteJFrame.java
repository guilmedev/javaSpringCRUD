    //Função para listar as disciplinas do db
    private void ListaDisciplinas(){    
        
        //Zera a lista 
        jComboBox1.removeAllItems();
        //Depois atualiza
        try {
            //Instancia da conexão db
            Connection connection = ConexaoDB.getInstance().getConnection();
            
            //Cria o query para trazer todos os nomes da tabela disciplina
            String query = "SELECT nome FROM disciplinas";
            //Executa o query
            ResultSet result =  connection.createStatement().executeQuery(query);
            //Enquanto houver resultado ...
            while(result.next()){
                //Pega o nome da disciplina na coluna nome
                String disciplinaNome = result.getString("nome");
                //Preenche o comboBox         
                ((DefaultComboBoxModel)jComboBox1.getModel()).addElement(disciplinaNome);                
        }
            
            
            /*Se a lista estiver vazia, desabilita o botão de excluir, pois
            não tem o que excluir
            */
            if(jComboBox1.getItemCount() == 0){
               jButtonExcluir1.hide();               
               jLabelAviso.show();
            }else{
                jButtonExcluir1.show();
                jLabelAviso.hide();                
            }
            
        } catch (SQLException ex) {
            
            ex.printStackTrace();
        }
        
    
        
        
        
    }
    

